#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	const char *tmp;

	tmp = s;
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (NULL);
}
